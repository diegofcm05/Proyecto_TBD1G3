    //Metodo utilizado para confirmar que la cadena ingresada es un double
    public boolean EsUnDouble(String x) {
        int pointcounter = 0;
        for (int i = 0; i < x.length(); i++) {
            if ((x.charAt(i) < 48 || x.charAt(i) > 57) && x.charAt(i) != 46) {
                return false;
            }
            if (x.charAt(i) == 46){
                pointcounter++;
            }
        }
        
        if (pointcounter > 1){
            return false;
        }
        return true; 
    }
    
    
